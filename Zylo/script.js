const carousel = document.getElementById('carousel');


function scrollLeft() {
  carousel.scrollBy({
    left: -320,
    behavior: 'smooth'
  });
}

function scrollRight() {
  carousel.scrollBy({
    left: 320,
    behavior: 'smooth'
  });
}

